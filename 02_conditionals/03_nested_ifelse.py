size = input('Enter size (small/medium/large): ').lower()

if size == 'small':
    print('You have selected small size, price is 10.')
elif size == 'medium':
    print('You have selected medium size,price is 15.')
elif size == 'large':
    print('You have selected large size,price is 20.')
else:
    print('Invalid size selected.')
   